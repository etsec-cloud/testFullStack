        <p class="margin60px">
          <span class="roseFont">• Tout salarié en CDD ou ex-CDD </span
          >justifiant d’une ancienneté de 24 mois (consécutifs ou non) au cours
          des 5 dernières années, dont 4 mois en CDD (consécutifs ou non) au
          cours des 12 derniers mois. Le salarié en CDD devra être toujours en
          poste lorsqu'il déposera son dossier de demande de financement et sa
          formation devra débuter au maximum 6 mois après la fin de son contrat.
